def load_jsonl(filepath):
    data = {}
    if not os.path.exists(filepath):
        return data
    with open(filepath, 'r') as f:
        for line in f:
            if line.strip():
                try:
                    item = json.loads(line)
                    # Handle list wrapper [ { ... } ] which might be generated by some workers incorrectly
                    if isinstance(item, list) and len(item) > 0 and isinstance(item[0], dict):
                        item = item[0]
                        
                    if isinstance(item, dict) and 'uuid' in item:
                        data[item['uuid']] = item
                except json.JSONDecodeError:
                    pass
    return data

def normalize_instance(instance):
    if isinstance(instance, str):
        return {instance.strip()}
    elif isinstance(instance, list):
        return {i.strip() for i in instance}
    return set()

def run_distributed_logic(input_file, output_file, workers, log_dir, random_sample=0, seed=42):
    """Core distributed runner logic"""
    # Generate a unique session ID
    session_id = uuid.uuid4().hex[:8]
    tmp_dir = os.path.join("tmp", session_id)
    
    # Create subdirectories for better organization
    tmp_input_dir = os.path.join(tmp_dir, "input")
    tmp_output_dir = os.path.join(tmp_dir, "output")
    tmp_log_dir = os.path.join(tmp_dir, "log")
    
    os.makedirs(tmp_input_dir, exist_ok=True)
    os.makedirs(tmp_output_dir, exist_ok=True)
    os.makedirs(tmp_log_dir, exist_ok=True)
    
    if not os.path.exists(input_file):
        print(f"Error: Input file {input_file} not found.")
        return False

    with open(input_file, 'r') as f:
        data = json.load(f)
    
    # Global Random Sampling
    if random_sample > 0 and random_sample < len(data):
        import random
        random.seed(seed)
        print(f"Randomly sampling {random_sample} items from {len(data)} total items (Seed: {seed}).")
        data = random.sample(data, random_sample)

    total_items = len(data)
    if total_items == 0:
        print("Input file is empty.")
        return True

    if workers > total_items:
        workers = total_items
        
    chunk_size = ceil(total_items / workers)
    
    print(f"Running distributed session: {session_id}")
    print(f"Items: {total_items}, Workers: {workers}")
    
    processes = []
    temp_files = []
    
    os.makedirs(os.path.dirname(os.path.abspath(output_file)), exist_ok=True)

    # Split and Launch
    for i in range(workers):
        start_idx = i * chunk_size
        end_idx = min((i + 1) * chunk_size, total_items)
        
        if start_idx >= total_items:
            break
            
        chunk_data = data[start_idx:end_idx]
        
        temp_input = os.path.join(tmp_input_dir, f"temp_input_{i}.json")
        temp_output = os.path.join(tmp_output_dir, f"temp_output_{i}.jsonl")
        worker_log = os.path.join(tmp_log_dir, f"worker_{i}.log")
        
        temp_files.append({
            "input": temp_input,
            "output": temp_output,
            "worker_log": worker_log
        })
        
        with open(temp_input, 'w') as f:
            json.dump(chunk_data, f, indent=2, ensure_ascii=False)
            
        cmd = [
            sys.executable, "main.py",
            "--batch",
            "--input", temp_input,
            "--output", temp_output,
            "--log-dir", log_dir
        ]
        
        with open(worker_log, "w") as log_f:
            p = subprocess.Popen(cmd, stdout=log_f, stderr=subprocess.STDOUT)
            processes.append(p)
    
    # Monitor Loop
    try:
        with tqdm(total=total_items, unit="case", desc="Progress") as pbar:
            while True:
                running = [p.poll() is None for p in processes]
                
                total_processed_count = 0
                for temp in temp_files:
                    out_file = temp["output"]
                    if os.path.exists(out_file):
                        try:
                            with open(out_file, 'rb') as f:
                                total_processed_count += sum(1 for _ in f)
                        except Exception:
                            pass
                
                if total_processed_count > pbar.n:
                    pbar.update(total_processed_count - pbar.n)
                
                if not any(running):
                    break
                
                time.sleep(1)
            
    except KeyboardInterrupt:
        print("\nStopping all processes...")
        for p in processes:
            p.terminate()
        # Fall through to merge results so we can save partial progress
        print("Saving partial results...")

    # Merge Results
    with open(output_file, 'w') as outfile:
        for temp in temp_files:
            t_out = temp["output"]
            if os.path.exists(t_out):
                with open(t_out, 'r') as infile:
                    for line in infile:
                        outfile.write(line)

    # Cleanup
    try:
        shutil.rmtree(tmp_dir)
    except Exception:
        pass
        
    return True

def calculate_accuracy(predictions, gt_map):
    if not gt_map: return 0.0, 0, 0
    correct = 0
    total = 0
    for uid, item in predictions.items():
        if uid in gt_map:
            total += 1
            gt_inst = normalize_instance(gt_map[uid].get('instance', ''))
            pred = item.get('component', '').strip()
            if pred in gt_inst:
                correct += 1
    return (correct / total if total > 0 else 0.0), correct, total

